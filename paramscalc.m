%-----------------------------------------------------------------------------------------------------------------------------------
% obtains parameter values
%-----------------------------------------------------------------------------------------------------------------------------------

%-----------------------------------------------------------------------------------------------------------------------------------
function params = paramscalc(system,simul)

%-----------------------------------------------------------------------------------------------------------------------------------
%	loading system variables
	struct2vars(system);
%	loading simulation variables
	struct2vars(simul);
%-----------------------------------------------------------------------------------------------------------------------------------

	L =						L1;
	alph =					L2/L;
	bet =					Delt/L;
	gamm =					H/L;
	kapp =					bslipper/L;
	et =					bslipamp/L;
	ph0 =					((bet*kapp)/(gamm^3))*((muvisc*Q)/((min(lambd+2*G))*L^2));
	
	params.L =				L;
	params.alph =			alph;
	params.bet =			bet;
	params.gamm =			gamm;
	params.kapp =			kapp;
	params.et =				et;
	params.ph0 =			ph0;
	
end
